export function getRecommendedVideos() {
  const result = []
  const date = new Date()
  let e = 0
  let i = 0
  rootLoop: do {
    date.setDate(date.getDate() - Math.pow(2, e++))
    const formatDate = `${date.getFullYear()}-${(date.getMonth() + 1).toString().padStart(2, '0')}-${date.getDate().toString().padStart(2, '0')}`
    do {
      if (i === videoList.length) {
        break rootLoop
      }
      const video = videoList[i]
      if (video.uploadDate === formatDate) {
        result.push(video)
      } else if (video.uploadDate < formatDate) {
        break
      }
      i++
    } while (true)
  } while (true)
  return result
}